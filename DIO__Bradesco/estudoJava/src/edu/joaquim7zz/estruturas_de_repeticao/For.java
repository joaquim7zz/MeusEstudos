package edu.joaquim7zz.estruturas_de_repeticao;

public class For {
    public static void main(String[] args) {
        for (int carneirionhos = 1; carneirionhos <= 20; carneirionhos++) {
            System.out.println("Carneirinho: " + carneirionhos);
        }
        System.out.println("Acabou");
    }
}
