package ParImpar;

public class ParImpar {
        public static void main(String[] args) {
            int numero = 7;

            if (numero % 2 == 0) {
                System.out.println("O número " + numero + " é par.");
            } else if (numero % 2 == 1) {
                System.out.println("O número " + numero + " é impar.");
            }
        }
    }
